package day_5;

public class forLoop_break {

	public static void main(String[] args) {
		
		// break
		// continue
		
	
      for(int d = 1; d<=10; d++) {
			
			if(d==5) {
				break;
			} System.out.println(d);
	
			}

		for(int a=0; a<=10; a++) {
			
			if (a==5) {
				continue;
			}
			System.out.println(a);
	}

}
}