package day_11;

public class Encap1 {

	
	private String name; 
	private int salary;
	
	public static void main(String[] args) {
		
// setter method and getter method with public access modifiers
	}
	
	public void setName(String name) {
		
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
	public int getSalary() {
		
		return salary;
	}

}
