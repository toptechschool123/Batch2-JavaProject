package day_3;

public class Special_Character {

	public static void main(String[] args) {

  // special character   \"   
	
			System.out.println(" I live in \"Seattle\" WA");
			
		// \n =new line
			
			System.out.println( " I  live in seattle\n but work in CA");
			
			// \t = tab
			System.out.println( " he is working\teveryday");
			
			// \\  = \
			
			System.out.println(" He is from \\ Afghanistan ");
			
			

	}

}
